public class Operator {
    public static void main (String[] args){
        
        int a = 10;
        int b = 4;
        int hasil;
         
       
  
        //penjumlahan
       hasil =(a+b);
       System.out.println(a + "+" + b + "=" + hasil ); 
       
       hasil =(a-b);
       System.out.println(a + "-" + b + "=" + hasil ); 
       
       hasil = (a-b);
       System.out.printf(" %d - %d  = %d  \n", a,b,hasil );
       
       hasil = (a*b);
       System.out.printf(" %d x %d  = %d  \n", a,b,hasil );
       
       
      
       float c = (float)a/b;
       
       System.out.println(a + " + " + b + " = " + c);
       
        
    }
    
}
